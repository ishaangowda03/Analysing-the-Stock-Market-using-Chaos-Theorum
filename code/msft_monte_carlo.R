# ── Monte Carlo simulation function ──────────────────────────────────────────
monte_carlo_simulation <- function(data, num_simulations, num_days) {
  
  # Compute log returns once (not inside loop)
  daily_returns <- diff(log(data$Adj_Close))
  mean_return <- mean(daily_returns)
  std_dev_return <- sd(daily_returns)
  
  # Initialize simulation matrix
  simulations <- matrix(0, nrow = num_days, ncol = num_simulations)
  simulations[1, ] <- data$Adj_Close[length(data$Adj_Close)]
  
  # Simulate price paths using geometric Brownian motion
  for (i in 2:num_days) {
    simulations[i, ] <- simulations[i - 1, ] * exp(mean_return + std_dev_return * rnorm(num_simulations))
  }
  
  return(simulations)
}

